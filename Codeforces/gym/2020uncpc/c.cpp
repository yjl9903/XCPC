using namespace std;
using ll = long long;
using PII = pair<int,int>;
const int mod = 1e9 + 7;
const int inf = 1 << 30;
const int maxn = 5000000 + 5;

inline int add(int x, int y) {
  x += y;
  return x >= mod ? x -= mod : x;
}
inline int sub(int x, int y) {
  x -= y;
  return x < 0 ? x += mod : x;
}
inline int mul(int x, int y) {
  return 1ll * x * y % mod;
}
inline int qpow(int x, ll n) {
  int r = 1;
  while (n > 0) {
    if (n & 1) r = 1ll * r * x % mod;
    n >>= 1; x = 1ll * x * x % mod;
  }
  return r;
}
inline int inv(int x) {
  return qpow(x, mod - 2);
}

int S, n, f[maxn];

int main() {
  scanf("%d%d", &S, &n);
  for (int i = 1; i <= n; i++) {
    f[i] = add(f[i], qpow(S, i));
    for (int j = i + i; j <= n; j += i) {
      f[j] = sub(f[j], f[i]);
    }
  }
  for (int i = 2; i <= n; i++) {
    f[i] = add(f[i], f[i - 1]);
  }
  printf("%d\n", f[n]);
  return 0;
}