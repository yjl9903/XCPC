inline int add(int x, int y) {
    x += y;
    return x >= mod ? x -= mod : x;
}
inline int sub(int x, int y) {
    x -= y;
    return x < 0 ? x += mod : x;
}
inline int mul(int x, int y) {
    return 1ll * x * y % mod;
}
inline int qpow(int x, ll n, int mod = mod) {
    int r = 1;
    while (n > 0) {
        if (n & 1) r = 1ll * r * x % mod;
        n >>= 1; x = 1ll * x * x % mod;
    }
    return r;
}
inline int inv(int x) {
    return qpow(x, mod - 2);
}

int main() {
    int T; scanf("%d", &T);
    const int inv2 = inv(2);
    while (T--) {
        ll n; scanf("%lld", &n);
        int ans = qpow(inv2, n % (mod - 1), mod);
        printf("%d\n", mul(ans, n % mod * 2 % mod));
    }
    return 0;
}