inline int add(int x, int y) {
  x += y;
  return x >= mod ? x -= mod : x;
}
inline int sub(int x, int y) {
  x -= y;
  return x < 0 ? x += mod : x;
}
inline int mul(int x, int y) {
  return 1ll * x * y % mod;
}
inline int qpow(int x, ll n) {
  int r = 1;
  while (n > 0) {
    if (n & 1) r = 1ll * r * x % mod;
    n >>= 1; x = 1ll * x * x % mod;
  }
  return r;
}
inline int inv(int x) {
  return qpow(x, mod - 2);
}

const int inv2 = inv(2);

int main() {
  int T; scanf("%d", &T);
  while (T--) {
    ll n;
    scanf("%lld", &n);
    if (n == 0) {
      puts("4");
    } else {
      int ans = qpow(2, n);
      ans = add(ans, 1);
      ans = add(ans, mul(qpow(3, n), qpow(inv2, n - 1)));
      printf("%d\n", ans);
    }
  }
  return 0;
}